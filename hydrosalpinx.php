                          <h1 style="padding-top:50px;">What is Hydrosalpinx and why is it a concern?</h1>
                      
                        <p class="blg">When the fallopian tubes get blocked or damaged, swollen and filled with fluid, it is called Hydrosalpinx. Fallopian tubes are attached to the uterus on either side and are responsible to pick up the eggs from the ovary every month and this is where the sperm meets the egg.</p>
                          
                        <p class="blg">The fertilization happens in these tubes and the embryo travels across the fallopian tubes and enters the uterus where it gets attached to the wall and develops into a baby. This process is not possible when the tubes are blocked and this poses a major concern for couples who find it difficult to have a baby. This results in abnormal pregnancies with complications Eg. Ectopic pregnancy which mostly can be life threatening.</p>

                        <h1>What Causes Hydrosalpinx?</h1>

                        <p class="blg">Tubal disease is responsible for one third of all female factor infertility, and the prevalence of hydrosalpinx is as high as 30% in women with tubal pathology. Although in-vitro fertilization (IVF) was first developed to treat tubal infertility, it was soon observed that patients with hydrosalpinx had a poor outcome after IVF treatment.</p>

                        <h1>How common is this?</h1>

                        <p class="blg">Usually any damage to the tubes, infection or inflammation will result in swollen and fluid filled tubes. Other reasons like surgeries directly in the tubes, adhesions of the uterus wall or pelvis, sexually transmitted infection or other infections like TB or Chlamydia.</p>

                        <h1>What are the symptoms of Hydrosalpinx?</h1>

                        <p class="blg">Usually females with Hydrosalpinx go asymptomatic for a longer period till they start the process of fertility treatment or conception, Difficulty in fertilisation or infertility is the first sign of Hydrosalpinx. Few females may sometimes present with pelvic pain and unusual vaginal discharge.</p>

